<p>KwBot jest botem IRC-owym. Jest to supybot, napisany w pythonie.</p>

<h1 id='main_commands'>Główne komendy:</h1>
<p><strong>Każda komenda musi być poprzedzona backtickiem (<code>`</code>) lub <code>KwB.</code></strong></p>
<ul>
<li><strong><code>commands</code></strong> &#151; pokazuje komendy bota.</li>
<li><strong><code>more</code></strong> &#151; pokazuje dalszą część wiadomości zakończonej <em>(X more messages)</em></li>
<li><strong><code>g</code>, <code>google</code></strong> &#151; przeszukuje Google.</li>
<li><strong><code>tr</code>, <code>translate</code></strong> &#151; tłumaczy przez Google Translate.</li>
<li><strong><code>seen</code></strong> &#151; pokazuje ostatnią wiadomość użytkownika na kanale i datę wysłania.</li>
</ul>
